package ejerciciosPildoras;


public class BucleFor {

	public static void main(String[] args) {

		for(int i=0; i<10;i++) {
			
			System.out.print((i+1)+ ". ");
			System.out.println("Mati");
		}
		
		
		for(int i=10; i>0;i--) {
			
			System.out.print((i)+ ". ");
			System.out.println("Mati");
		}
		
		
	}

}
